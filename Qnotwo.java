//Write a Java program that declares two integers and prints their sum, difference, product, and quotient
public class Qnotwo {
    int i = 1, j = 2;
    public static void main(String[] args) {
        Qnotwo obj = new Qnotwo();
        System.out.println(obj.i + obj.j);
        System.out.println(obj.i - obj.j);
        System.out.println(obj.i * obj.j);
        System.out.println(obj.i / obj.j);
    }
}